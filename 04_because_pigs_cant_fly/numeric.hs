module Numeric where

{-
  Integral numbers
    - whole
    - positive and negative

  Int:     fixed-precision numbers with a lower and upper limit

  Integer: arbitrarily large or small numbers
-}

{-
  Fractional numbers
    - non whole

  Float:      single-precision floating point numbers

  Double:     double-precision floating point numbers with twice as many bits as
              Float numbers

  Rational:   fractional number that represents a ratio of two Integers

  Scientific: space efficient almost-arbitrary precision scientific number
              provided by the scientific package
-}
